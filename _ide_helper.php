namespace Illuminate\Support {
    class Str {
        /**
         * Split string by new lines.
         *
         * @see \NormanHuth\Library\Support\Macros\Str\SplitNewLinesMacro::__invoke()
         * @param string $string
         * @return array
         * @static
         */
        public static function splitNewLines($string)
        {
            return \Illuminate\Support\Str::splitNewLines($string);
        }
    }
}
